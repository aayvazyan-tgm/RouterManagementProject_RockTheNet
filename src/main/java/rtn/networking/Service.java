package rtn.networking;

/**
 * Represents the service for which a policy was made.
 */
public class Service
{
	private String name;

    public Service(String name) {
        this.name = name;
    }

    /**
     * Gets the name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the name.
     *
     * @param name the new name
     */
    public void setName(String name) {
        this.name = name;
    }
}